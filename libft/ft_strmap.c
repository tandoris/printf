#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	char	*str;
	int		cmpt;

	cmpt = 0;
	if (!s)
		return (NULL);
	str = ft_strnew(ft_strlen(s));
	if (!str)
		return (NULL);
	while (s[cmpt])
	{
		str[cmpt] = (f)(s[cmpt]);
		cmpt++;
	}
	return (str);
}
