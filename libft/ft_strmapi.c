#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char			*str;
	unsigned int	cmpt;

	cmpt = 0;
	if (!s)
		return (NULL);
	str = ft_strnew(ft_strlen(s));
	if (!str)
		return (NULL);
	while (s[cmpt])
	{
		str[cmpt] = (f)(cmpt, s[cmpt]);
		cmpt++;
	}
	return (str);
}
