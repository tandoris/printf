#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	cmpt;
	char	*a;

	cmpt = 0;
	a = s;
	if (n > 0)
	{
		while (cmpt < n)
		{
			a[cmpt] = 0;
			cmpt++;
		}
	}
}
